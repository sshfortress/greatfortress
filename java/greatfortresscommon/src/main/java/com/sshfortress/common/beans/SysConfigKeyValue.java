package com.sshfortress.common.beans;


/**
 * 系统 key value
 *
 */

public class SysConfigKeyValue {

    private String ckey;

    private String cvalue;

    /**
     * @return ckey
     */

    public String getCkey() {
        return ckey;
    }

    /**
     * @param ckey
     */
    public void setCkey(String ckey) {
        this.ckey = ckey;
    }

    /**
     * @return cvalue
     */

    public String getCvalue() {
        return cvalue;
    }

    /**
     * @param cvalue
     */
    public void setCvalue(String cvalue) {
        this.cvalue = cvalue;
    }

}
